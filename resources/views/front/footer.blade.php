        function changeArea(all)
        {
          
          val = all.value;
           if(val == 1 || val == 5 || val == 10 || val == 0)
           {
             reload_search("search");
           }
           if(val == 4)
           {
             reload_search("search_house");
           }

           if(val == 8)
           {
             reload_search("search_plot");
           }
           if(val == 3)
           {
             reload_search("search_hotel");
           }
           if(val == 2 || val == 7 || val == 6 || val == 9)
           {
             reload_search("search_commercial");
           }


        }


        
        function reload_search(val)
        {
          
          var url = site + "/reload_search";
          var xml = new XMLHttpRequest();
          var t = document.getElementById('t_').value;
          xml.open("POST", url, true);
          xml.setRequestHeader("X-CSRF-TOKEN", t);

          var fd = new FormData();

          fd.append("val", val);


        xml.onreadystatechange = function() 
        {
            if (xml.status == 419) {
                //takeBackToLogin();
                location.reload();
            }
            if (xml.readyState == 4 && xml.status == 200) 
            {
              document.getElementById("search_zone").innerHTML = "";
              document.getElementById("search_zone").innerHTML = xml.responseText;
            }
        }
        xml.send(fd);
   
