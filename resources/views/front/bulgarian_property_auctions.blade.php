  <p><strong>Auction day finance and bidding</strong><br>
You must have all your finances in order. Before bidding, a deposit is required, the amount varies depending upon the auction rules, but is between 10 - 30% of the starting price. This deposit must be paid into the auctioneers bank account and will be refunded only if your bid is unsuccessful. Now begins the exciting part of the process, you must arrive at the auction before the published time and again prove your identity. At the start of the proceedings all sealed bids will be opened. If you are lucky yours will be the only one, in which case the final price will be your starting bid.
