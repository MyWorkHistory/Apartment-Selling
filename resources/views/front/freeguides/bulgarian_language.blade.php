          <p><strong>Unique Letters</strong>Some of the letters that do not exist in the Latin alphabet can be more difficult to pronounce, but the Bulgarian people know this and are usually very helpful and understanding. The letter ‘Ч’ is pronounced as ‘ch’;  ‘Ц’ is pronounced ‘ts’; ‘Ш’ is ‘sh’; ‘Щ’ is ‘sht’ and ‘Ж’ is ‘zh’ which with a little practice, will become familiar. The easiest way to learn them is to associate them with English words. ‘Ч’ is like in the beginning of ‘China, chair or chat’; ‘Ц’ is like in beginning of ‘Tsunami or tsar’; ‘Ш’ like in the beginning of ‘she or short’ and ‘Ж’ is easily associated to the middle sound in ‘vision or illusion’. ‘Щ’ can be more difficult for some, but sounds very similar to the middle sound of ‘ashtray or fishtail’.
           Other letters unique to Cyrillic are ‘Я’, which is ‘ya’ which sounds like ‘young or yard’ and ‘Ю’, which is ‘yu’ like in ‘unique or you’.
         </p>
