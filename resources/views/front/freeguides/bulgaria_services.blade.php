              <p><strong>Connecting to Services in Bulgaria </strong><br> Once you have bought your Bulgarian property and decided when you are going to move in, you will want to arrange the connection of essential services such as electricity and water, so how do you get connected?</p>
            </div>
          
        


            <div>
           <p><strong>Electricity </strong> <br> In the North-Central region of Bulgaria the main provider is E.ON. You will need to apply to E.ON to have your electricity connected and sign a contract specifying the power supply to be installed. If it's a new property you will need to prove you're the owner by producing a copy of the title deeds or if you are renting in Bulgaria, a copy of the lease. Usually you also need to produce your passport or residence permit. If you plan to pay by direct debit from a bank or post office account, don't forget to take along your account details.</p>
           <img src="{{asset('assets/images/staticpages/new/connect1.jpg')}}" alt="image of a house">
            </div>


            <div>
            <p>
            If you're moving into an old property, you must also tell E.ON the name of the person who previously paid the bills (which will be on the title deeds). The bills are then transferred into your name, usually for a nominal fee. Please be aware that unpaid utility bills of the previous owners can often be transferred on to your account. When setting up your account it is wise take along a Bulgarian speaker so that you fully understand what is going on and make sure everything is in order.
            </p>
           </div>


           <div>
            <p><strong>Meters</strong><br>
            In Bulgaria, meters are usually installed in a box on an outside wall of a property. However, if your meter isn't accessible from outside and the property isn't permanently occupied, make sure you either leave the keys with a neighbour or make arrangements to have your meter read. If your meter cannot be read, you will receive an estimate based on previous bills, although it must be read at least once a year.
            </p>
           </div>


           <div>
           <img src="{{asset('assets/images/staticpages/new/connect2.jpg')}}" alt="">
           <p><strong>BILLS</strong> <br> BillsYou're normally billed for your electricity each month. Bills can be paid by direct debit, in cash at the post office (Post Bank). By ePay.bg, Transcard payment card or at the E.ON customer services centres in major towns. It's also possible to pay a fixed amount each month by standing order based on your estimated usage; at the end of the year you will receive a bill for the amount owing or a rebate of the amount overpaid. These methods of payment are preferable, particularly if it is a holiday home or you're a non-resident. If you want to set up a direct debit or standing order at your bank to pay utility bills, you will need to take a copy of the deeds to your house, your limited company registration papers and someone who speaks Bulgarian.</p>
            </div>

            <div>
            <p><strong>Power cuts</strong><br>In rural Bulgaria the power supply can often weaken or fail totally, sometimes for a few minutes, sometimes for several hours, and on occasions, for days. Power cuts are fairly frequent in some areas, especially during thunderstorms, heavy rain and heavy snowfalls. If you live in an area with an unreliable electricity supply it's advisable to buy an uninterruptable power supply (UPS) with a battery back-up, which will allow you up to 20 minutes to shut down a computer or other vital equipment when the power shuts off. You may also consider buying a generator for essential electrical items such as a kettle, microwave, television, particularly for cuts during the winter months. You may also consider having a cooker that has both electricity and calor gas as a source of power and keep some candles or oil lamps handy.
            </p>  
            <img src="{{asset('assets/images/staticpages/new/connect3.jpg')}}" alt="">
           </div>

           <div>
            <p>If the power keeps tripping off when you attempt to use a number of high-powered appliances simultaneously, or the lights dim when you turn on the cooker, it probably means that the rating of your power supply is too low. If this is the case, you need to ask the electricity company to upgrade the power supplied to your property (ie install 3 phase), although your standing charge will be higher for this supply.</p>
           </div>

           <div>
            <p><strong>Electrical Appliances </strong> <br>If you are coming to Bulgaria from outside of Europe or the UK, generally it will be much easier to buy new appliances once you have moved into your Bulgarian property and prices are comparable to those elsewhere in Europe. However with smaller items such as toasters, kettles and hair-dryers it is possible to use an adapter or get a qualified electrician to change the plugs. Of course if you are moving from elsewhere in Europe, there should be no problem with voltage or plugs on your household white goods, but you may consider that the age of the items and that the cost of transportation outweighs their value.</p>
           </div>

           <div>
            <p><strong>Mains Gas </strong> <br>The gas network in Bulgaria is currently at the fledgling stage, although 'gasification' is expanding. The north-eastern region of Bulgaria is served by the company Overgas, the largest retailer of natural gas in Bulgaria, and its website (www.overgas.bg ) includes a map of the towns connected to mains gas.
When moving into a property already connected to mains gas, you will need to contact the local distribution company to have the gas switched on and/or have the meter read and the account transferred to your name. If you want to be connected to mains gas, the cost depends on the amount of gas you expect to use (e.g. for heating or just for cooking), the average connection charge being around 300 lev (a‚¬150). Gas is currently around 40% cheaper than electricity.</p>
           </div> 


           <div>
           <img src="{{asset('assets/images/staticpages/new/connect4.jpg')}}" alt="">
            <p><strong>Bottled Gas </strong> <br>Many Bulgarian rural homes have cookers that use bottled gas. Check when moving into a property that the gas bottle isn't empty. Keep a spare bottle or two handy and make sure you know how to change the bottles, as this can be quite a complicated procedure.
Bottles can be purchased at most petrol stations or delivery can often be arranged through your local mayor. Some houses keep their gas bottles outside, however, if you do this you must buy propane gas rather than butane, as it can withstand a greater range of temperatures, which is for internal use only (in fact, propane gas bottles must be kept outside).</p>
           </div> 

           <div>
           <p><strong> Wood </strong> <br> Bulgaria relies heavily on wood for heating and hot water, especially during the winter months. Traditionally the heating systems in Bulgarian homes would be by the Jamal (traditional heating system) in each room, however these day most homes have at least one fireplace or stand alone wood burner and many also have back boilers for radiators and hot water.
Log deliveries are ordered by the cubic metre and can usually be arranged through your mayor or local woodcutter. Wood will usually be delivered by truck in the form of large uncut logs that will have to be cut down and split into a manageable size to fit in the fireplace. Wood should be well seasoned before it is burnt and the bigger the logs the longer they take to dry out so it is best to have them cut and then stacked and protected from the rain for at least two years before burning so that they season properly. Unseasoned logs generate less heat because they contain a large amount of moisture, they will also cause a build-up of tar and soot in the chimney that can be the cause of chimney fires. When ordering your wood for the first time, order both seasoned (for immediate use) and unseasoned (for the following year(s)).</p>
          </div> 

          <div>
            <p><strong>Water</strong><br>
            Generally the tap water all over Bulgaria is very safe to drink and most properties in Bulgarian towns and villages are connected to mains water. If you buy a new property in Bulgaria, in all likelihood, connection will already be organised by the developer. However, some older Bulgarian rural properties and Bulgarian property developments will need a connection, the cost of which will depend on the type of soil and terrain that must be dug through to lay the pipes. Always check before buying a property without a connection how much it will cost and work this into your budget.
            </p>
             <img src="{{asset('assets/images/staticpages/new/connect5.jpg')}}" alt="">
          </div>

          <div>
            <p>Properties that are connected to mains water are metered and charged per cubic metre, there is no standing charge and you will be billed on a monthly basis. When moving into a new property, if not already arranged, you can set up an account at the local municipal offices. Ask the water company to read your meter and transfer the account to your name.<br>
            Water cuts are rare, but can occur if there is any maintenance work being done on the old leaking pipes and generally you will receive no notice of these cuts, so you could keep a back-up supply to hand, however bottled mineral water is available everywhere and is very inexpensive.</p>
          </div> 

          <div>
          <img src="{{asset('assets/images/staticpages/new/connect6.jpg')}}" alt="">
           <p><strong>Sewage & Waste water</strong><br>Properties in urban areas are usually connected to mains drainage, whereas those in rural areas usually have individual sewage systems.
If your property has a septic tank, it should be sited at least 3 metres within the boundaries of your property in a position that will enable it to be emptied easily, although this will only have to be done after 3 to 5 years. Before buying a property with its own septic tank get it checked out by a professional to ensure it is in good condition and working properly.
Before buying a property that needs a septic tank installed or plot for building your own house in Bulgaria, get advice on where the system should be sited and the costs involved.
</p>
