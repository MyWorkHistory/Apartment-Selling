  <p><strong>Telecommunications</strong><br>You will notice that all Bulgarians have a mobile phone and seem to talk on them constantly. The three main mobile service providers are Mtel(Vodafone), Globul and Vivacom and between them cover the whole country with the exception of a few mountainous regions. The call rates are very competitive, Pay as you Go, and internet packages are also available.
Should you require a fixed line, this is available through BTC (Vivacom), however, most people are happy to solely use their mobile phones. Should you be going backwards and forwards to your home country, it can be useful to get Prepaid SIM cards for both Bulgaria and your home country and swap them over, just remember to send a text every 3-4 months to keep the SIM active, this way friends and family will have numbers for you wherever you are.</p>
